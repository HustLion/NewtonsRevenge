function love.conf(t)
    t.title = "Newton's Revenge" -- The title of the window the game is in (string)
    t.author = "ChispaTeam"      -- The author of the game (string)
    t.screen.width = 960         -- The window width (number)
    t.screen.height = 800        -- The window height (number)
    t.screen.fullscreen = false  -- Enable fullscreen (boolean)
    t.modules.joystick = false   -- Enable the joystick module (boolean)
    t.modules.mouse = false      -- Enable the mouse module (boolean)
end